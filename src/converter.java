    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String tx = (String)jComboBox1.getSelectedItem ();
              
        if(jRadioButton1.isSelected()&&tx.equals("Fahrenheit"))
        {
            String text = jTextField1.getText ();
            float Cel = Float.parseFloat(text);
            float Fah = (float) (Cel * 1.8 + 32);
            jTextField2.setText(""+Fah);
        }
            
        if(jRadioButton1.isSelected()&&tx.equals("Kelvin"))
        {
            String text = jTextField1.getText();
            float Cel = Float.parseFloat(text);
            float Kel = (float) (Cel+273.15);
            jTextField2.setText(""+ Kel);
        }
        
        if(jRadioButton2.isSelected()&&tx.equals("Celsius"))
        {
            String text = jTextField1.getText();
            float Fah = Float.parseFloat(text);
            float Cel = (float) ((float) (Fah - 32)/1.8);
            jTextField2.setText(""+ Cel);
        }
       if(jRadioButton2.isSelected()&&tx.equals("Kelvin"))
        {
            String text = jTextField1.getText();
            float Kel = Float.parseFloat(text);
            float Fah = (float) (Kel + 255.92778);
            jTextField2.setText(""+ Fah);
        } 
       
       if(jRadioButton3.isSelected()&&tx.equals("Celsius"))
        {
            String text = jTextField1.getText();
            float Cel = Float.parseFloat(text);
            float Kel = (float) (Cel-273.15);
            jTextField2.setText(""+ Kel);
        }
       
        if(jRadioButton3.isSelected()&&tx.equals("Fahrenheit"))
        {
            String text = jTextField1.getText();
            float Fah = Float.parseFloat(text);
            float Kel = (float) (Fah - 457.87);
            jTextField2.setText(""+ Kel);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

